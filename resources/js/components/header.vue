  const lastScrollY = ref(0)
  const scrollThreshold = 10 // Minimum scroll distance to trigger hide/show

  // Computed property for header classes
  const headerClasses = computed(() => {
    // On mobile (lg-), always show header (no hide/show functionality)
    if (window.innerWidth < 1024) {
      return {
        'transform translate-y-0': true,
        'transform -translate-y-full': false,
      }
    }
    
    // On desktop (lg+), apply hide/show functionality
    return {
      'transform translate-y-0': isHeaderVisible.value,
      'transform -translate-y-full': !isHeaderVisible.value,
    }
  })

  // Scroll handler function
  function handleScroll() {
    const currentScrollY = window.scrollY
    
    // Update shadow state
    isScrolled.value = currentScrollY > 20
    
    // Disable hide/show functionality on mobile (lg-)
    if (window.innerWidth < 1024) {
      isHeaderVisible.value = true
      return
    }
    
    // Only process visibility if scroll difference is above threshold (desktop only)
    if (Math.abs(currentScrollY - lastScrollY.value) < scrollThreshold) {
      return
    }
    
    // Show header when scrolling up or at top of page (desktop only)
    if (currentScrollY < lastScrollY.value || currentScrollY < 100) {
      isHeaderVisible.value = true
    } 
    // Hide header when scrolling down (and not at top) (desktop only)
    else if (currentScrollY > lastScrollY.value && currentScrollY > 100) {
      isHeaderVisible.value = false
    }
    
    lastScrollY.value = currentScrollY
  }

  // Throttle function to improve performance
  function throttle(func, limit) {
    let inThrottle
    return function() {
      const args = arguments
      const context = this
      if (!inThrottle) {
        func.apply(context, args)
        inThrottle = true
        setTimeout(() => inThrottle = false, limit)
      }
    }
  }

  // Create throttled scroll handler
  const throttledScrollHandler = throttle(handleScroll, 16) // ~60fps

  // Add keyboard shortcut handler
  function handleKeydown(event) {
    if ((event.ctrlKey || event.metaKey) && event.key === 'k') {
      event.preventDefault()
      openSearchModal()
    }
  }

  // Add event listeners on mount
  onMounted(() => {
    window.addEventListener('keydown', handleKeydown)
    window.addEventListener('scroll', throttledScrollHandler, { passive: true })
    
    // Initialize scroll position
    lastScrollY.value = window.scrollY
    
    // Ensure header is visible on mobile
    if (window.innerWidth < 1024) {
      isHeaderVisible.value = true
    }
  })

  // Remove event listeners on unmount
  onUnmounted(() => {
    window.removeEventListener('keydown', handleKeydown)
    window.removeEventListener('scroll', throttledScrollHandler)
  })

  function openSearchModal() {
    searchModalOpen.value = true
  }

